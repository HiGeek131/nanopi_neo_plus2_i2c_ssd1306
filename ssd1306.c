void ssd1306WriteCom(uint8_t dat)
{
	wiringPiI2CWriteReg8(ssd1306_fd, SSD1306COM, dat);
}

void ssd1306WriteDat(uint8_t dat)
{
	wiringPiI2CWriteReg8(ssd1306_fd, SSD1306DAT, dat);
}

void ssd1306Flush(uint8_t fill_Data)
{
	uint8_t m, n;
	for(m = 0; m < 8; m ++)
	{
		ssd1306WriteCom(0xb0+m);		//page0-page7
		ssd1306WriteCom(0x00);		//low column start address
		ssd1306WriteCom(0x10);		//high column start address
		for(n = 0; n < 128; n ++)
			{
				ssd1306WriteDat(fill_Data);
			}
	}
}

void ssd1306Display(uint8_t b,uint8_t *p)
{
	uint8_t x, a = 0;
	ssd1306WriteCom(0xb0+b);
	ssd1306WriteCom(0x00);
	ssd1306WriteCom(0x10);
	for(a=0; a < 21 && p[a] != '\0'; a++)
	{
		for(x  =0; x < 6; x ++)
		{
			ssd1306WriteDat(zi[p[a] - 32][x]);
		}	
	}
    for(; a < 21; a++)
	{
		for(x  =0; x < 6; x ++)
		{
			ssd1306WriteDat(0x00);
		}	
	}
}

void ssd1306Init()
{
	ssd1306WriteCom(0xae);	//0xae关闭显示0xaf开启显示
	ssd1306WriteCom(0x20);	//设置内存地址模式	
	ssd1306WriteCom(0x10);	//00,水平寻址模式;01,垂直寻址模式;10,页寻址模式;11,无效
	ssd1306WriteCom(0xb0);	//设置第几页,0-7
	ssd1306WriteCom(0x00);	//设置列低位地址
	ssd1306WriteCom(0x10);	//设置列高位地址
	ssd1306WriteCom(0x40);	//设置列起始行 此项大约与页面平移有关系
	ssd1306WriteCom(0x81);	//设置对比度
	ssd1306WriteCom(0xff);	//对比度值
	ssd1306WriteCom(0xa1);	//a0 内存列0映射到点阵列0 a1 内存列127映射到点阵列0  左右颠倒
	ssd1306WriteCom(0xc8);	//设置OLED点阵的扫描方向 c0，com0-comn-1 c8，反之  上下颠倒
	ssd1306WriteCom(0xa6);	//a6 标准模式 a7 反显模式
	ssd1306WriteCom(0xa8);	//设置MUX比例，此项效果暂不明确
	ssd1306WriteCom(0x3F);	//64  ???1f
	ssd1306WriteCom(0xa4);	//0xa4,输出RAM内容;0xa5,忽略RAM内容
	ssd1306WriteCom(0xd3);	//设置行输出补偿 此项大约与页面平移有关系
	ssd1306WriteCom(0x00);	//输出补偿为0
	
	ssd1306WriteCom(0xd5);	//设置工作频率与分频器
	ssd1306WriteCom(0xf0);	//高位为时钟频率 低位为分频器
	ssd1306WriteCom(0xd9);	//设置预充电周期
	ssd1306WriteCom(0x22);	//低位为第一周期 高位为第二周期
	
	ssd1306WriteCom(0xda);	//com硬件设置
	ssd1306WriteCom(0x12);	//选择pin模式 默认设置为12，工作不正常  0x02 128*32  0x12 128*64
	ssd1306WriteCom(0xdb);	//设置com电压 不知道干啥的
	ssd1306WriteCom(0x20);	//0x20,0.77xVcc 默认设置
	ssd1306WriteCom(0x8d);	//电荷泵设置
	ssd1306WriteCom(0x14);	//14开启 10关闭
	ssd1306WriteCom(0xaf);	//0xae关闭显示0xaf开启显示
}
